// 基础类型
(function () {
    // 布尔类型
    var bool = true;
    console.log(bool);
    // 数字类型
    var a1 = 10; // 十进制
    var a2 = 10; // 二进制
    var a3 = 10; // 八进制
    var a4 = 0xa; // 十六进制
    console.log(a1, a2, a3, a4);
    // 字符串类型
    var str = '哈哈哈哈';
    var str2 = '呵呵呵呵';
    var str3 = '呃呃呃呃';
    console.log(str, str2, str3);
    console.log(str + a1);
    // undefind 和 null
    var undef = undefined;
    var nll = null;
    console.log(undef);
    console.log(nll);
})();
